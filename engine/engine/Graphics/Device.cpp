#include "engine/pch.h"
#include "Device.hpp"

////////////////////////////////////////////////////////////////
//////////////////////////// Define ////////////////////////////
////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////
//////////////////////////// Static ////////////////////////////
////////////////////////////////////////////////////////////////
S<Device> gDevice = nullptr;
////////////////////////////////////////////////////////////////
/////////////////////// Standalone Function /////////////////////
////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////
///////////////////////// Member Function //////////////////////
////////////////////////////////////////////////////////////////
Device::~Device()
{
}

Device& Device::Get()
{
   return *gDevice;
}

Device& Device::Init(Window& window)
{
   if(nullptr == gDevice) {
      gDevice = S<Device>(new Device());
      ASSERT_DIE(gDevice->RhiInit(window), "Fail to init Device");
   }

   return *gDevice;
}

